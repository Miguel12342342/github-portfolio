let currentLang = localStorage.getItem('lang') || 'pt';

function applyTranslations(lang) {
    const t = translations[lang];

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (!t[key]) return;
        if ('i18nHtml' in el.dataset) {
            el.innerHTML = t[key];
        } else {
            el.textContent = t[key];
        }
    });

    window.typewriterPhrases = t.typewriter;

    // Placeholders de inputs (atributo, não conteúdo)
    const cmdInputEl = document.getElementById('cmd-input');
    if (cmdInputEl && t.cmd_placeholder) {
        cmdInputEl.setAttribute('placeholder', t.cmd_placeholder);
    }
}

document.addEventListener('DOMContentLoaded', () => {
    applyTranslations(currentLang);

    const langToggle = document.getElementById('langToggle');
    if (langToggle) {
        langToggle.innerText = currentLang === 'pt' ? '🇺🇸 EN' : '🇧🇷 PT';

        langToggle.addEventListener('click', () => {
            currentLang = currentLang === 'pt' ? 'en' : 'pt';
            localStorage.setItem('lang', currentLang);
            langToggle.innerText = currentLang === 'pt' ? '🇺🇸 EN' : '🇧🇷 PT';
            applyTranslations(currentLang);
        });
    }
});
